package HjelpeKlasser;

public class NameTranslations {
	
	private String eng;
	private String esp;
	private String fra;
	public String getEng() {
		return eng;
	}
	public void setEng(String eng) {
		this.eng = eng;
	}
	public String getEsp() {
		return esp;
	}
	public void setEsp(String esp) {
		this.esp = esp;
	}
	public String getFra() {
		return fra;
	}
	public void setFra(String fra) {
		this.fra = fra;
	}
	
	

}
